void BGBDT_RandomTickRegion(BGBDT_VoxWorld *world,
	BGBDT_VoxRegion *rgn, BGBDT_VoxCoord xyz)
{
	BGBDT_VoxTypeInfo *tyi;
	int tix, vty;
	BGBDT_VoxData td;
	int i;

	i=BGBDT_WorldGetVoxelData(world, xyz, &td, NULL,
		BGBDT_TRFL_NOLOAD);
	if(i<0)
		return;

	vty=td.vtypel|(td.vtypeh<<8);
	tix=vty&4095;
	tyi=world->voxtypes[tix];
	if(tyi && tyi->RandomTick)
		{ tyi->RandomTick(world, rgn, xyz, td); }
}

